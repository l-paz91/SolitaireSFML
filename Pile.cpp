//--INCLUDES--//
#include "Pile.h"

#include <algorithm>
#include <random>

// -----------------------------------------------------------------------------

Pile::Pile()
{

}

// -----------------------------------------------------------------------------

Pile::~Pile()
{
	for (Card* card : mCards)
	{
		if (card)
		{
			delete card;
			card = nullptr;
		}
	}
}

// -----------------------------------------------------------------------------

void Pile::push(const Card& pCard)
{
	mCards.push_back(new Card(pCard));
}

// -----------------------------------------------------------------------------

Card* Pile::pop()
{
	Card* card = mCards.back();
	mCards.pop_back();
	return card;
}

// -----------------------------------------------------------------------------

Card* Pile::peek()
{
	return mCards.back();
}

// -----------------------------------------------------------------------------

void Pile::shuffle()
{
	std::random_device rd;
	std::mt19937 g(rd());
	std::shuffle(mCards.begin(), mCards.end(), g);
}

